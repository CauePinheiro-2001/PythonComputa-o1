'''8.Sabendo que a área de um triângulo é a medida da sua base multiplicada pela
medida  de  sua altura   dividida por   dois(Área=   (b*h)/2).
Escreva  um  programa  que  peça  os  valores  da base e da altura e forneça
a área do triângulo.'''

print('Programa 8')
h = float(input('Altura do triângulo: '))
b = float(input('Base do triângulo: '))
area =   (b*h)/2
print('A área do triângulo é: ', round(area,2))


