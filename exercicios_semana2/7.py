'''7.Elaborar um programa que calcule e apresente o  volume  de  uma  caixa  retangular,
valores devem informados pelo usuário.Volume = Comprimento * Largura * Altura'''

print('Programa 7')
C = float(input('Comprimeto: '))
L = float(input('Largura: '))
A = float(input('Altura: '))
V = C*L*A
print('O volume da caixa retangular é (L): ', round(V,2))
