'''5.Faça um programa que leia dois números reais e  em  seguida  mostre:
a  soma,  o  produto,  a divisão e a subtração entre eles.'''

print('Programa 5')
x = float(input('Valor 1: '))
y = float(input('Valor 2: '))
s = x + y
p = x * y
d = x / y
sub = x - y
print(' A soma : {0} \n O prudto: {1} \n A divisão : {2} \n A subtração: {3}'.format (s,p,d,sub))
