'''Tuplas são semelhantes as listas, entretando imutáveis'''

# As Tuplas tem essa estrutura:
tupla = ('item1', 'item2', 'item3')
print(tupla)

# Colocar uma segunda tupla com o mesmo nome:
tuplas = ('Caue', 'Gabi', 'Christian')
# Substitui a tupla toda.
tuplas = ('Andreia', 'Bruna', 'Douglas')
print(tuplas)


