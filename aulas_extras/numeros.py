'''Python trata os numeros de diversas maneiras diferentes'''

# Inteiros (você pode somar(+), subtrair(-), multiplicar(*), dividir(/)):
print(10+5)
print(10-5)
print(10*5)
print(10/5)

# Exponenciais (**):
print(3**2)

# Parenteses para modificar as ordens de execução:
print((2+3)*4)

# Numeros de ponto flutuante (float):
print(0.1 + 0.1)

# Evitando erros: utilize a seguinte sintaxe para numeros em strings.
age = 20
print('FELIZ ' + str(age) + ' ANOS!')


