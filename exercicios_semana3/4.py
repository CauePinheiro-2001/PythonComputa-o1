'''4.Escreva   um   programa   que   lê   um   número inteiro.
Informe   se   o   número   é   positivo, negativo ou igual a zero.'''
x = int(input('Insira um número inteiro:'))
if x > 0:
    print('Esse numero é positivo')
elif x == 0:
    print('Esse numero é igual a 0')
else:
    print('Esse numero é negativo')